/**
 * enum class for point color
 * 
 * When the user sets cluster option and click run, cluster handler should set point
 * color with this enum. Renderer then uses this enum to draw point color via the controller. 
 * 
 * @author Yeongbae Jeon
 * @version 1.0
 */
public enum PointColor {
    RED, BLUE, NONE
}
